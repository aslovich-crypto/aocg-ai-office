RULES = [
    ("Топливо",    ["лукойл", "газпром", "роснефть", "шелл", "bp", "татнефть", "азс"]),
    ("Продукты",   ["магнит", "пятёрочка", "пятерочка", "перекрёсток", "перекресток",
                    "ашан", "лента", "дикси", "вкусвилл", "азбука вкуса"]),
    ("Транспорт",  ["яндекс.такси", "ситимобил", "делимобил", "uber", "максим"]),
    ("Питание",    ["макдоналдс", "kfc", "burger king", "шоколадница", "кофемания",
                    "starbucks", "вкусно и точка"]),
    ("Прочее",     ["ozon", "wildberries", "озон", "вайлдберриз"]),
]

def auto_categorize(org: str) -> str:
    lower = org.lower()
    for category, keywords in RULES:
        if any(kw in lower for kw in keywords):
            return category
    return "Не указано"
