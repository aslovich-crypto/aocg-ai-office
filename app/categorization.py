def categorize_items(items) -> Optional[str]:
    """Категория по позициям чека (Фикс №4). Каждую позицию матчим по ITEM_TRIGGERS
    (lower, подстрока, первая статья в порядке dict), суммируем item['sum'] по статьям.
    Победитель — статья с МАКС суммой среди узнанных; при всех нулевых суммах — по числу
    узнанных позиций. None, если не узнана ни одна позиция (тогда вызывающий идёт на org)."""
    if not items:
        return None
    sums: dict = {}
    counts: dict = {}
    for item in items:
        name = (item.get("name") or "").lower()
        if not name:
            continue
        for article, words in ITEM_TRIGGERS.items():
            if any(w in name for w in words):
                try:
                    amount = float(item.get("sum")) if item.get("sum") is not None else 0.0
                except (TypeError, ValueError):
                    amount = 0.0
                sums[article] = sums.get(article, 0.0) + amount
                counts[article] = counts.get(article, 0) + 1
                break
    if not counts:
        return None
    if any(v > 0 for v in sums.values()):
        return max(sums, key=lambda a: sums[a])
    return max(counts, key=lambda a: counts[a])


def categorize(org_text: str, items=None) -> str:
    """Единая точка категоризации (Фикс №4): позиции → магазин → «Прочие хозрасходы».
    Анализ позиций в приоритете; если ничего не узнано — фолбэк на имя орг
    (auto_categorize_v2, который сам падает в DEFAULT_FALLBACK)."""
    return categorize_items(items) or auto_categorize_v2(org_text)
